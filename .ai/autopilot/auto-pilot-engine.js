#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const { execSync } = require('child_process');

class AutoPilotEngine {
    constructor(repoPath = process.cwd()) {
        this.repoPath = repoPath;
        this.checklistPath = path.join(repoPath, '.ai', 'autopilot', 'checklists', 'production-readiness.json');
        this.reportsPath = path.join(repoPath, '.ai', 'autopilot', 'reports');
        this.buildersPath = path.join(repoPath, '.ai', 'autopilot', 'builders');
        this.ensureDirectories();
    }

    ensureDirectories() {
        [this.reportsPath, this.buildersPath].forEach(dir => {
            if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
        });
    }

    loadChecklist() {
        const data = fs.readFileSync(this.checklistPath, 'utf8');
        return JSON.parse(data);
    }

    saveChecklist(checklist) {
        fs.writeFileSync(this.checklistPath, JSON.stringify(checklist, null, 2));
    }

    analyzeSystem() {
        console.log('🔍 Analyzing system for gaps...\n');
        
        const checklist = this.loadChecklist();
        const gaps = [];
        const humanActions = [];
        
        for (const [category, data] of Object.entries(checklist.categories)) {
            console.log(`📁 ${category.toUpperCase()}:`);
            for (const check of data.checks) {
                // Simulate checking if feature exists
                const exists = this.checkIfExists(check.id);
                if (!exists && data.required) {
                    check.status = 'missing';
                    gaps.push(check);
                    console.log(`   ❌ MISSING: ${check.name} (Builder: ${check.builder_needed})`);
                } else if (!exists && !data.required) {
                    check.status = 'optional';
                    console.log(`   ⚠ OPTIONAL: ${check.name} - ${check.human_action || 'Auto-builder available'}`);
                    if (check.human_action) {
                        humanActions.push({ check: check.name, action: check.human_action });
                    }
                } else {
                    check.status = 'present';
                    console.log(`   ✅ PRESENT: ${check.name}`);
                }
            }
            console.log('');
        }
        
        return { gaps, humanActions, checklist };
    }

    checkIfExists(checkId) {
        // Simple existence check - can be expanded
        const existingFiles = this.scanForFeatures();
        
        if (checkId.includes('AUTH')) return existingFiles.includes('auth');
        if (checkId.includes('PAY')) return existingFiles.includes('payment');
        if (checkId.includes('PAGE')) return existingFiles.includes('pages');
        if (checkId.includes('TEST')) return existingFiles.includes('test');
        if (checkId.includes('DEPLOY')) return existingFiles.includes('deploy');
        
        return false;
    }

    scanForFeatures() {
        const features = [];
        const domainsPath = path.join(this.repoPath, 'admin-portal', 'js', 'domains');
        if (fs.existsSync(domainsPath)) {
            const domains = fs.readdirSync(domainsPath);
            features.push(...domains);
        }
        return features;
    }

    generateBuilderForGap(gap) {
        console.log(`\n🔧 Generating builder for: ${gap.name}`);
        
        const builderContent = `# ============================================================
# BUILDER: ${gap.builder_needed}
# Fixes: ${gap.name}
# Generated by Auto-Pilot Engine
# ============================================================

param([string]$OutputPath = "C:\\Users\\Maya\\NewDigitalShop\\docker-fix\\admin-portal")

Write-Host ""
Write-Host "Building: ${gap.name}" -ForegroundColor Cyan
Write-Host ""

# TODO: Implement the builder for ${gap.name}
# This builder will be generated when the Auto-Pilot runs

Write-Host "✅ ${gap.name} built successfully!" -ForegroundColor Green
`;
        
        const filename = `Build-${gap.builder_needed}.ps1`;
        const filepath = path.join(this.buildersPath, filename);
        fs.writeFileSync(filepath, builderContent);
        
        console.log(`   ✓ Builder saved: ${filename}`);
        return filepath;
    }

    generateHumanActionReport(humanActions) {
        if (humanActions.length === 0) return null;
        
        const report = `# Human Action Required Report
Generated: ${new Date().toISOString()}

## Actions You Need to Take

${humanActions.map(h => `### ${h.check}
- **Action Required:** ${h.action}
- **Status:** Pending
- **Estimated Time:** 5-10 minutes

`).join('\n')}

## Instructions

For each action above:
1. Complete the required setup (create account, get API keys, etc.)
2. Add the credentials to environment variables
3. Mark this item as complete

When all actions are complete, re-run the Auto-Pilot to continue.
`;
        
        const reportPath = path.join(this.reportsPath, 'human-actions-required.md');
        fs.writeFileSync(reportPath, report);
        return reportPath;
    }

    generateProgressReport(checklist, gaps) {
        const totalChecks = Object.values(checklist.categories).reduce((acc, cat) => acc + cat.checks.length, 0);
        const completedChecks = Object.values(checklist.categories).reduce((acc, cat) => acc + cat.checks.filter(c => c.status === 'present').length, 0);
        const percent = (completedChecks / totalChecks) * 100;
        
        const report = `# Auto-Pilot Progress Report
Generated: ${new Date().toISOString()}

## Overall Progress

| Metric | Value |
|--------|-------|
| Total Checks | ${totalChecks} |
| Completed | ${completedChecks} |
| Progress | ${percent.toFixed(1)}% |
| Gaps Found | ${gaps.length} |

## Category Breakdown

${Object.entries(checklist.categories).map(([cat, data]) => {
    const catTotal = data.checks.length;
    const catCompleted = data.checks.filter(c => c.status === 'present').length;
    return `### ${cat.toUpperCase()}
- Progress: ${((catCompleted/catTotal)*100).toFixed(0)}%
- Missing: ${data.checks.filter(c => c.status === 'missing').length}
- Optional: ${data.checks.filter(c => c.status === 'optional').length}`;
}).join('\n\n')}

## Next Steps

${gaps.length > 0 ? '1. Run the Auto-Pilot again to generate missing builders\n2. Execute the generated builders\n3. Re-run analysis' : '✅ System analysis complete! Ready for human review.'}

---
*Generated by Vi Auto-Pilot Engine*
`;
        
        const reportPath = path.join(this.reportsPath, `autopilot-report-${new Date().toISOString().split('T')[0]}.md`);
        fs.writeFileSync(reportPath, report);
        return reportPath;
    }

    async run() {
        console.log('\n✈️ VI AUTO-PILOT ENGINE - System Analysis\n');
        console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n');
        
        const { gaps, humanActions, checklist } = this.analyzeSystem();
        
        console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n');
        
        if (gaps.length > 0) {
            console.log(`🔧 Generating builders for ${gaps.length} gaps...\n`);
            for (const gap of gaps) {
                this.generateBuilderForGap(gap);
            }
            console.log(`\n✅ ${gaps.length} builders generated!`);
        }
        
        if (humanActions.length > 0) {
            console.log(`\n👤 HUMAN ACTIONS REQUIRED: ${humanActions.length}\n`);
            for (const h of humanActions) {
                console.log(`   📋 ${h.check}: ${h.action}`);
            }
            const humanReport = this.generateHumanActionReport(humanActions);
            console.log(`\n   📄 Detailed report: ${humanReport}`);
        }
        
        const reportPath = this.generateProgressReport(checklist, gaps);
        console.log(`\n📊 Progress report saved: ${reportPath}`);
        
        console.log('\n━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━');
        console.log(`\n✈️ Auto-Pilot Analysis Complete!`);
        console.log(`\n📈 Current Readiness: ${((Object.values(checklist.categories).reduce((acc, cat) => acc + cat.checks.filter(c => c.status === 'present').length, 0) / Object.values(checklist.categories).reduce((acc, cat) => acc + cat.checks.length, 0)) * 100).toFixed(1)}%`);
        
        if (gaps.length === 0 && humanActions.length === 0) {
            console.log(`\n🎉 SYSTEM IS PRODUCTION READY! 🎉`);
        } else if (gaps.length === 0) {
            console.log(`\n⏳ Waiting for human actions: ${humanActions.length} items remaining`);
        } else {
            console.log(`\n🚧 Gaps found: ${gaps.length}. Builders generated. Run them to continue.`);
        }
        console.log('');
    }
}

if (require.main === module) {
    const engine = new AutoPilotEngine();
    engine.run().catch(console.error);
}

module.exports = AutoPilotEngine;