#!/usr/bin/env node

const fs = require('fs');
const path = require('path');

class PredictiveScaffolder {
    constructor(repoPath = process.cwd()) {
        this.repoPath = repoPath;
        this.adminPortalPath = path.join(repoPath, 'admin-portal');
        this.templatesPath = path.join(repoPath, '.ai', 'templates');
        this.predictionsPath = path.join(repoPath, '.ai', 'data', 'predictions');
        this.ensureDirectories();
        this.domainKeywords = this.loadDomainKeywords();
    }

    ensureDirectories() {
        [this.templatesPath, this.predictionsPath].forEach(dir => {
            if (!fs.existsSync(dir)) fs.mkdirSync(dir, { recursive: true });
        });
    }

    loadDomainKeywords() {
        return {
            shops: ['shop', 'store', 'marketplace', 'vendor', 'seller', 'ecommerce', 'product'],
            competitors: ['competitor', 'rival', 'market', 'uris', 'watch', 'track', 'analyze'],
            intelligence: ['intelligence', 'insight', 'feed', 'knowledge', 'ai', 'learn', 'predict'],
            evolution: ['evolution', 'evolve', 'adapt', 'mutate', 'grow', 'cycle', 'improve'],
            values: ['values', 'ethics', 'ethical', 'constraint', 'rule', 'policy', 'principle'],
            alerts: ['alert', 'notification', 'monitor', 'watchdog', 'detect', 'warning'],
            vault: ['vault', 'secret', 'key', 'token', 'credential', 'stripe', 'api'],
            reports: ['report', 'analytics', 'dashboard', 'metric', 'kpi', 'chart', 'stat'],
            settings: ['settings', 'config', 'preference', 'option', 'setup', 'admin']
        };
    }

    detectDomainFromText(text) {
        const lowerText = text.toLowerCase();
        const matches = [];
        
        for (const [domain, keywords] of Object.entries(this.domainKeywords)) {
            for (const keyword of keywords) {
                if (lowerText.includes(keyword)) {
                    matches.push({ domain, keyword, confidence: 0.7 });
                    break;
                }
            }
        }
        
        if (matches.length === 0) {
            matches.push({ domain: 'unknown', keyword: 'generic', confidence: 0.3 });
        }
        
        return matches.sort((a, b) => b.confidence - a.confidence)[0];
    }

    checkExistingDomain(domain) {
        const domainPath = path.join(this.adminPortalPath, 'js', 'domains', domain);
        return fs.existsSync(domainPath);
    }

    generateScaffold(domain, confidence) {
        const scaffoldPath = path.join(this.adminPortalPath, 'js', 'domains', domain);
        
        if (fs.existsSync(scaffoldPath)) {
            return { success: false, message: `Domain '${domain}' already exists`, path: scaffoldPath };
        }
        
        fs.mkdirSync(scaffoldPath, { recursive: true });
        fs.mkdirSync(path.join(scaffoldPath, 'components'), { recursive: true });
        fs.mkdirSync(path.join(scaffoldPath, 'pages'), { recursive: true });
        
        const apiContent = `// ${domain} Domain API
// Auto-generated by Predictive Scaffolder (confidence: ${(confidence * 100).toFixed(0)}%)

import { apiClient } from '../../core/api/client.js';

export const ${domain}Api = {
    // TODO: Implement API methods for ${domain}
    getAll: () => apiClient.get('/api/v1/${domain}'),
    getById: (id) => apiClient.get(\`/api/v1/${domain}/\${id}\`),
    create: (data) => apiClient.post('/api/v1/${domain}', data),
    update: (id, data) => apiClient.put(\`/api/v1/${domain}/\${id}\`, data),
    delete: (id) => apiClient.delete(\`/api/v1/${domain}/\${id}\`)
};

window.${domain}Api = ${domain}Api;
`;
        fs.writeFileSync(path.join(scaffoldPath, 'api.js'), apiContent);
        
        const indexContent = `// ${domain} Domain - Barrel Export
export * from './api.js';
export * as ${domain}Components from './components/index.js';

if (typeof window !== 'undefined') {
    window.${domain}Domain = {
        api: window.${domain}Api
    };
}
`;
        fs.writeFileSync(path.join(scaffoldPath, 'index.js'), indexContent);
        
        const readmeContent = `# ${domain.toUpperCase()} Domain

## Auto-Generated (Predictive Scaffolder)

**Confidence: ${(confidence * 100).toFixed(0)}%**

### TODO List:
- [ ] Implement API endpoints in backend
- [ ] Add domain-specific components
- [ ] Create page templates
- [ ] Add store slice
- [ ] Update routing configuration

### Suggested API endpoints:
- GET    /api/v1/${domain}
- GET    /api/v1/${domain}/{id}
- POST   /api/v1/${domain}
- PUT    /api/v1/${domain}/{id}
- DELETE /api/v1/${domain}/{id}
`;
        fs.writeFileSync(path.join(scaffoldPath, 'README.md'), readmeContent);
        
        return { success: true, message: `Scaffolded '${domain}' domain`, path: scaffoldPath, files: ['api.js', 'index.js', 'README.md', 'components/', 'pages/'] };
    }

    watchForChanges() {
        console.log('👁️ Predictive Scaffolder is watching for requirements...');
        console.log('   (Run with --once to analyze once, or --watch for continuous)');
    }

    async runOnce() {
        console.log('\n🔮 Predictive Scaffolder - Analyzing Requirements\n');
        console.log('━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n');
        
        const requirementsPath = path.join(this.predictionsPath, 'pending-requirements.json');
        const predictions = [];
        
        if (fs.existsSync(requirementsPath)) {
            const pending = JSON.parse(fs.readFileSync(requirementsPath, 'utf8'));
            for (const req of pending.requirements) {
                const detection = this.detectDomainFromText(req.text);
                const exists = this.checkExistingDomain(detection.domain);
                
                predictions.push({
                    requirement: req.text,
                    source: req.source,
                    detected_domain: detection.domain,
                    confidence: detection.confidence,
                    already_exists: exists,
                    timestamp: new Date().toISOString()
                });
                
                if (!exists && detection.confidence > 0.6) {
                    const result = this.generateScaffold(detection.domain, detection.confidence);
                    console.log(`   ✓ ${result.message}`);
                }
            }
        } else {
            console.log('   ℹ No pending requirements found.');
            console.log('   Add requirements to: .ai/data/predictions/pending-requirements.json');
            console.log('');
            console.log('   Example format:');
            console.log('   {');
            console.log('     "requirements": [');
            console.log('       { "text": "We need a payment domain", "source": "manual" }');
            console.log('     ]');
            console.log('   }');
        }
        
        const outputPath = path.join(this.predictionsPath, 'predictions-log.json');
        fs.writeFileSync(outputPath, JSON.stringify(predictions, null, 2));
        console.log(`\n   ✓ Predictions saved: predictions-log.json`);
        
        return predictions;
    }
}

if (require.main === module) {
    const scaffolder = new PredictiveScaffolder();
    const args = process.argv.slice(2);
    
    if (args.includes('--watch')) {
        scaffolder.watchForChanges();
    } else {
        scaffolder.runOnce().catch(console.error);
    }
}

module.exports = PredictiveScaffolder;